function merge (series1, series2) {
    for (var i = 0; i<series2.length; i++) {
    series1.push (series2 [i]);
  }
  return series1.sort();
}
console.log(merge([ 4, 5, 6 ], [ 1, 2, 3, 4 ]), "=?", [ 1, 2, 3, 4, 4, 5, 6 ]);
